class PokerHand
  def initialize(deck)
    @hand = []
    @rank_count = Hash.new(0)

    5.times do
      card = deck.draw
      @hand << card
      @rank_count[card.rank] += 1
    end
  end

  def print
    @hand.each { |card| puts card }
  end

  def evaluate
    case
    when royal_flush?     then 'Royal flush'
    when straight_flush?  then 'Straight flush'
    when four_of_a_kind?  then 'Four of a kind'
    when full_house?      then 'Full house'
    when flush?           then 'Flush'
    when straight?        then 'Straight'
    when three_of_a_kind? then 'Three of a kind'
    when two_pair?        then 'Two pair'
    when pair?            then 'Pair'
    else                       'High card'
    end
  end

  private

  def royal_flush?
    return if @hand.map(&:suit).uniq.count > 1
    @hand.map(&:value).sort == [10, 11, 12, 13, 14]
  end

  def straight_flush?
    flush? && straight?
  end

  def four_of_a_kind?
    @rank_count.values.one?(4)
  end

  def full_house?
    @rank_count.values.sort == [2, 3]
  end

  def flush?
    hand_suit.uniq.size == 1
  end

  def straight?
    return false unless @rank_count.values.all?(1)
    @hand.max.value - @hand.min.value == 4
  end

  def three_of_a_kind?
    @rank_count.values.one?(3)
  end

  def two_pair?
    @rank_count.select { |_, v| v == 2 }.size == 2
  end

  def pair?
    @rank_count.values.one?(2)
  end

  def hand_suit
    @hand.map(&:suit)
  end
end

class Deck
  RANKS = ((2..10).to_a + %w(Jack Queen King Ace)).freeze
  SUITS = %w(Hearts Clubs Diamonds Spades).freeze

  def initialize
    reset
  end

  def draw
    reset if @deck.empty?
    @deck.pop
  end

  private

  def reset
    @deck = new_deck.shuffle
  end

  def new_deck
    RANKS.product(SUITS).map do |rank, suit|
      Card.new(rank, suit)
    end
  end
end

class Card
  include Comparable

  VALUES = { 'Jack' => 11, 'Queen' => 12, 'King' => 13, 'Ace' => 14 }.freeze

  attr_reader :rank, :suit

  def initialize(rank, suit)
    @rank = rank
    @suit = suit
  end

  def value
    VALUES.fetch(rank, rank)
  end

  protected

  def <=>(other_card)
    value <=> other_card.value
  end

  def to_s
    "#{rank} of #{suit}"
  end
end

